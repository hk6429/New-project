const genericWrongAnswers = ["一般百姓", "音樂", "書信", "戰爭", "官員", "月亮", "故鄉", "教師"];

function source(term: string, locator: string): QuestionSource {
  return {
    title: `教育部《重編國語辭典修訂本》：「${term}」`,
    author: "教育部國語推行委員會",
    url: `https://dict.revised.moe.edu.tw/search.jsp?la=0&powerMode=0&word=${encodeURIComponent(term)}`,
    citation: locator,
  };
}

function makeQuestion(seed: Seed, index: number): BorrowedWordsQuestion {
  const [term, referent, context, relation, locator] = seed;
  const distractors = genericWrongAnswers.filter((item) => item !== referent).slice(index % 4, index % 4 + 3);
  while (distractors.length < 3) distractors.push(genericWrongAnswers[distractors.length]);
  const type = index % 2 === 0 ? "term-match" : "context-match";
  return {
    id: `CA${String(index + 1).padStart(3, "0")}`,
    coreTopicId: `classic-${term}`,
    type,
    grade: index % 5 === 0 ? [8, 9] : [7, 8, 9],
    difficulty: index % 7 === 0 ? "advanced" : index % 3 === 0 ? "intermediate" : "basic",
    prompt: type === "term-match" ? term : context,
    options: [referent, ...distractors],
    correctAnswers: [referent],
    metonym: term,
    referent,
    relation,
    rationale: `「${term}」在此不是字面本義，而是透過「${relation}」代稱「${referent}」。`,
    context,
    clues: [`辨認「${term}」在句中的實際所指`, `檢查兩者是否具有「${relation}」的相關性`],
    distractorRationales: Object.fromEntries(distractors.map((item) => [item, `「${item}」與「${term}」的典故、特徵或本句語境沒有直接相關性。`])),
    source: source(term, locator),
    license: "official-reference; question-context-original; pending-review",
    creator: "借代偵探學院題庫批次 A",
    checkers: [],
    disputeNote: null,
    statistics: { attempts: 0, correct: 0, optionCounts: {} },
    version: 1,
    createdAt: "2026-07-12",
    updatedAt: "2026-07-12",
    reviewers: [],
    status: "pending-review",
  };
}

export const classicBankQuestions: BorrowedWordsQuestion[] = seeds.map(makeQuestion);
